    "#create function for geo coding addresses\n",
    "# simple library for geocoding ----  https://geocoder.readthedocs.io/ \n",
    "\n",
    "def geomapper(address):\n",
    "    try:\n",
    "        # remove leading \",\" from the address\n",
    "        address = remove_prefix(address, ',')\n",
    "        # check if address has already been geocoded, if so return that value from the geomapped_addresses dictionary\n",
    "        if (address in geomapped_addresses):\n",
    "            return geomapped_addresses[address]\n",
    "        g = geocoder.google(address, key=gmaps_api_key)\n",
    "        lat = g.json['lat']\n",
    "        long = g.json['lng']\n",
    "        latlong = f'{lat},{long}'\n",
    "        # store the lat/long in the geomapped_addresses dictionary so we don't run the API on it again\n",
    "        geomapped_addresses[address] = latlong\n",
    "        \n",
    "        if (len(geomapped_addresses) % 100 == 0):\n",
    "            print(f\"{len(geomapped_addresses)} addresses processed\")\n",
    "            file_name = \"Resources/geomapped_addresses_temp.json\"\n",
    "            with open(file_name, 'w') as fp:\n",
    "                json.dump(geomapped_addresses, fp, sort_keys=True, indent=4)\n",
    "        return latlong\n",
    "    except:\n",
    "        #error_count += 1\n",
    "        return None"
